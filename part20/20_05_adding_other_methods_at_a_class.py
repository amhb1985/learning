#adding other Methods in a Class

class Point():
    def __init__(self, x, y):
        self.x = x
        self.y = y
    def getX(slef):
        return self.x
    def getY(slef):
        return self.y
    

point1 = Point(10, 100)

#for printing both of our adding x and y we can use point1.x or .y
print(point1.x)
